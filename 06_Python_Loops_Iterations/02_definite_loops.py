# Definite Loops

for x in 'Hello':
    print(x)
print('---')

# -------------------------------------

for i in [5, 4, 3, 2, 1]:
    print(i)
print('---')

# ---------------------------------------

friends = ["Jose", "Glenn", "Sally"]
for friend in friends:
    print(friend)
print('---')

# ---------------------------------------

persons = [friend for friend in friends]
print(persons)
